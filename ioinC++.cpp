#include <iostream>

using namespace std;

int main(int argc, char *argv[]) {
    int num1 , num2;
    cout << " Enter the value of num1:\n";
    /* insertion operator */
    cin >> num1; /* extraction operator */
    cout << "Enter the value of num2:\n";
    cin >> num2;

    cout<<"The sum is " << num1 + num2;

    return 0;
}
